#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main()
{
    ll t;
    cin >> t;
    while (t--)
    {
        ll n;
        cin >> n;
        string s;
        cin >> s;
        vector<char> v;
        for (ll i = 0; i < n; i++)
        {
            v.push_back(s[i]);
        }
    }
}